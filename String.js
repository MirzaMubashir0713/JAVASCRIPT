//String
var str="       My name is mirza mubashir ali baig       ";
// String length
console.log(str.length);
// String Search from starting
console.log(str.indexOf("ali"));
// String Search from last
console.log(str.lastIndexOf("ali"));
// Sub String 
console.log(str.substring(17,34));
// string to lower case
console.log(str.toLowerCase());
// String to upper case
console.log(str.toUpperCase());
// String trim
console.log(str.trim());
console.log(str.trimStart());
console.log(str.trimEnd());
// string split
console.log(str.split(''));
// replace
console.log(str.replaceAll("name is","self"));
// String add at start and end
console.log(str.padStart(53,"hello"));
console.log(str.padEnd(51,"bye"));








